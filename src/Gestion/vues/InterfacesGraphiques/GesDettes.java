    private boolean testVide(){
        boolean flag = true;
        if(nomClient.getText().isEmpty() || montant_total.getText().isEmpty()){
            PagePrincipale.AfficherMessage("Les informations grisées doivent etre non null !!!",2);
        }else{
            if(avance.getText().isEmpty()){
                PagePrincipale.AfficherMessage("L'avance doit etre non null !!!",2);
            }else{
                flag = false;
            }
        }
        return flag;
    }
    
    private boolean testNombre(){
        boolean flag = false;
        int test = 0;
        try {
            int n = Gestion.utils.Utils.ObjectToInt(avance.getText());
            if(n<=0){
                PagePrincipale.AfficherMessage("Le montant de la charge est un nombre strictement positif !!!",2);
                test++;
            }
        } catch (Exception ex) {
            PagePrincipale.AfficherMessage("Le montant de la charge est un nombre !!!",2);
            test++;
        }
        if(test==0) flag = true;
        return flag;
    }
    
    private void save(){
        if(!testVide()){
            if(testNombre()){
                message = new JSONObject();
                requette = new JSONObject();
                int t = Integer.parseInt(montant.getText())-Integer.parseInt(avance.getText());
                String texte = "";
                if(t<=0){
                    if(t==0) texte = "Ce client a comblé sa dette !!! ";
                    else texte = "Ce client a comblé sa dette !!! veillez le rembourser "+(-t)+" FCFA";
                    JOptionPane.showMessageDialog(null,texte);
                    vente.setDate_de_solde((java.sql.Timestamp)Gestion.utils.Utils.getDate(null,0));
                    vente.setIdUserSoldeur(PageDeLogin.currentUser.getIdutilisateur());
                    vente.setTypeEnregistrement("vente");
                    String m = "Le "+(java.sql.Timestamp)Gestion.utils.Utils.getDate(null,0)+" "+PageDeLogin.currentUser.getNom()+" "+PageDeLogin.currentUser.getPrenom()+" a soldé"
                            + " la dette de ce Client de "+montant.getText()+" FCFA et lui a remboursé "+(-t)+" FCFA";
                    vente.setDescription(vente.getDescription()+"\n"+m);
                    vente.setDette(0);
                }else{
                    texte = "Ce client a encore une dette de "+t+" FCFA";
                    JOptionPane.showMessageDialog(null,texte);
                    vente.setDette(t);
                    vente.setTypeEnregistrement("pret");
                    String m = "Le "+(java.sql.Timestamp)Gestion.utils.Utils.getDate(null,0)+" "+PageDeLogin.currentUser.getNom()+" "+PageDeLogin.currentUser.getPrenom()+" a reduit"
                            + " la dette de ce Client de "+avance.getText()+" FCFA";
                    vente.setDescription(vente.getDescription()+"\n"+m);
                }
                System.out.println(vente.getDescription());
                JSONObject json = new JSONObject();
                json.put("1", vente);
                message.put("lesproduits",json);
                requette.put("message",message);
                requette.put("user",PageDeLogin.currentUser);
                requette.put("action",10);
                passerelle = new Passerelle(requette);
                retour = passerelle.ForwardQuery();
                if(retour.get("code").equals("0")){
                    PagePrincipale.AfficherMessage(Gestion.vues.Messages.MessagePasserelle.success_modifiervente,1);
                    LesDonnees.initData(0);
                    init();
                    vente = new VenteMetier();
                    peutSupprimer = false;
                    placerTableau(LesDonnees.lesdettes);
                }else{
                    PagePrincipale.AfficherMessage(Gestion.vues.Messages.MessagePasserelle.erreur_modifiervente,3);
                }
            }
        }
    }
    
    private void placerTableau(JSONObject data){
        Set cle = data.keySet();
        Object[][] donnee = new Object[data.size()][titres.length];
        int i = 0;
        lesID = new ArrayList();
        for (Iterator iterator = cle.iterator(); iterator.hasNext();) {
            Object next = iterator.next();
            lesID.add(next);
            donnee[i][0] = ((HashMap)data.get(next)).get("client");
            donnee[i][1] = ((HashMap)data.get(next)).get("user");
            donnee[i][2] = ((HashMap)data.get(next)).get("date_de_vente");
            donnee[i][3] = ((HashMap)data.get(next)).get("dette");
            i++;
        }
        tableau.setModel(new javax.swing.table.DefaultTableModel(donnee,titres));
    }
    
    private void actionTableau(){
        int n = tableau.getSelectedRow();
        HashMap data = (HashMap) LesDonnees.lesdettes.get(lesID.get(n));
        vente = new VenteMetier();
        vente.setIdVente((int) data.get("idvente"));
        vente.setDescription(data.get("description").toString());
        peutSupprimer = true;
        initAllData(data);
    }
    
