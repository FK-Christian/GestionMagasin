    private void recherche(){
        String m = Gestion.utils.Utils.getDate(date1,4).toString();
        String m1 = Gestion.utils.Utils.getDate(date2,4).toString();
        LesDonnees.getLesDettes("(V.date_de_vente between '"+m+"' and '"+m1+"')");
        if(LesDonnees.lesdettes.isEmpty()){
            PagePrincipale.AfficherMessage("pas de resultat !!!",2);
        }
        placerTableau(LesDonnees.lesdettes);
    }
    
    private void rechercheClient(){
        LesDonnees.getLesDettes("(concat(C.nom,' ',C.prenom) like('%"+nomClient.getText()+"%'))");
        if(LesDonnees.lesdettes.isEmpty()){
            PagePrincipale.AfficherMessage("pas de resultat !!!",2);
        }
        placerTableau(LesDonnees.lesdettes);
    }
    
