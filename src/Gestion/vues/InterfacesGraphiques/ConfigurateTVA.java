    private void Actionboutton2(){
        if(!listeTVA.isSelectionEmpty()){
            int n = listeTVA.getSelectedIndex();
            String[] data = listeTVA.getModel().getElementAt(n).toString().split(":");
            System.out.println((listeTVA.getModel().getSize()-2)+"  "+n);
            if((listeTVA.getModel().getSize()-2)!=n){               
                de.setValue(Integer.parseInt(data[1]));
                a.setValue(Integer.parseInt(data[3]));
                tva.setValue(Integer.parseInt(data[5]));
                lesTVA.remove(""+data[5]);
                listeTVA.setListData(JsonToArray(lesTVA));
            }else{
                montantlimite.setValue(Integer.parseInt(data[1]));
                tvalimite.setValue(Integer.parseInt(data[3]));
            }
        }
    }
    
