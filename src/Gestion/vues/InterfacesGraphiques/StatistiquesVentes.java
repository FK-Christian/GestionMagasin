    private void placerTableau(JSONObject data){
        donnee = new Object[data.size()][titres.length];
        Set cle = data.keySet();
        int i = 0;
        for (Iterator iterator = cle.iterator(); iterator.hasNext();) {
            Object next = iterator.next();
            donnee[i][0] = ""+((VenteMetier)data.get(next)).getIdVente();
            donnee[i][1] = ""+((VenteMetier)data.get(next)).getDate_de_vente();
            donnee[i][2] = ""+((VenteMetier)data.get(next)).getPrix_de_vente();
            donnee[i][3] = ""+((VenteMetier)data.get(next)).getTva();
            donnee[i][4] = ""+((VenteMetier)data.get(next)).getTypeEnregistrement();
            i++;
        }
        tableau.setModel(new javax.swing.table.DefaultTableModel(donnee,titres));
    }
    
    private JSONObject getSpecificVente(){
        JSONObject data = new JSONObject();
        data.put("message",new VenteMetier());
        java.sql.Timestamp date1 = (java.sql.Timestamp) Gestion.utils.Utils.getDate(dateP1,3);
        java.sql.Timestamp date2 = (java.sql.Timestamp) Gestion.utils.Utils.getDate(dateP2,3);
        String condition = "";
        if(PageDeLogin.currentUser.getTypeuser().equals("Vendeur")) condition = "utilisateuridUser = "+PageDeLogin.currentUser.getIdutilisateur()+" and ";
        condition += "((date_de_vente between '"+date1+"' and '"+date2+"')"
                + " or date_de_vente like('"+date1.toString().substring(0,10)+"%') or date_de_vente like('"+date2.toString().substring(0,10)+"%')) and etat=1";
        System.out.println(condition);
        String order = "idVente desc";
        data = LesDonnees.getManyObjet(13,condition, order);
        if((!data.get("code").equals("0"))||((JSONObject)data.get("message")).isEmpty()){
            JSONObject json = new JSONObject();
            data.put("message",json);
        }
        return (JSONObject)data.get("message");
    }
    
