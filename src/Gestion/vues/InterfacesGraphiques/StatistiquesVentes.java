    private void detail(){
        int n = tableau.getSelectedRow();
        vente = (VenteMetier) LesDonnees.lesventes.get(""+tableau.getValueAt(n,0));
        if(vente.getBenefice()<=0) visuel.setForeground(Color.red);
        else visuel.setForeground(Color.green);
        visuel.setText(vente.getDescription()+"\nLe benifice associer a cette vente est: \n \t"+vente.getBenefice()+"\n\n"+infogenerale);
    }
    
