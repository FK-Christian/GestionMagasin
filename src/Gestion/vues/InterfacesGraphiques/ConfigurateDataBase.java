    private void placerDonners(){
        url.setText(parametres.get("URL").toString());
        user.setText(parametres.get("UserName").toString());
        pass.setText(parametres.get("Password").toString());
        driver.setText(parametres.get("driver_class").toString());
        lang.setText(parametres.get("dialect").toString());
    }
    
    private void bouttonTestConnection(){
        recupererDonner();
        if(Gestion.utils.Utils.modificationBD(parametres,auDemarrage)){
            test = true;
            message.setForeground(Color.green);
            message.setText("Connection succeful !!!");
        }else{
            test = false;
            message.setForeground(Color.red);
            message.setText("Echec de Connection !!!");    
        }
    }
    
    private void recupererDonner(){
        parametres.put("URL",url.getText());
        parametres.put("UserName",user.getText());
        parametres.put("Password",pass.getText());
        parametres.put("driver_class",driver.getText());
        parametres.put("dialect",lang.getText());
    }
    
    private void bouttonvalider(){
        recupererDonner();
        Gestion.utils.Utils.setparam("Documents/parametres/paramBD.properties", parametres);
        this.setVisible(false);
    }

